//Assignment Operators
// 1. = --> equals to 
// 2. += --> add and equals to
// 3. -= --> sub and equala to
// 4. /= --> divide and equals to 
// 5. *= --> multiply and equals to
// 6. %= --> mod and equals to
// 7. **= --> to the power and equals to 

let a = 10 

console.log('a+= is ', a+= 1) //11
console.log('a-= is', a-= 1) //10
console.log('a/= is', a/=2) //5
console.log('a*= is', a*=2) //10
console.log('a**= is', a**=2) //100
console.log('a%=2', a%=2) //0